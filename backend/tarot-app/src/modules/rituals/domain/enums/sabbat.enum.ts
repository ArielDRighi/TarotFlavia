/**
 * Sabbats de la Rueda del Año (Wheel of the Year)
 */
export enum Sabbat {
  SAMHAIN = 'samhain', // 31 Oct (Norte) / 30 Abr (Sur) - Muerte y Ancestros
  YULE = 'yule', // 21 Dic (Norte) / 21 Jun (Sur) - Solsticio Invierno
  IMBOLC = 'imbolc', // 1 Feb (Norte) / 1 Ago (Sur) - Purificación
  OSTARA = 'ostara', // 21 Mar (Norte) / 21 Sep (Sur) - Equinoccio Primavera
  BELTANE = 'beltane', // 1 May (Norte) / 31 Oct (Sur) - Pasión y Unión
  LITHA = 'litha', // 21 Jun (Norte) / 21 Dic (Sur) - Solsticio Verano
  LAMMAS = 'lammas', // 1 Ago (Norte) / 1 Feb (Sur) - Cosecha y Gratitud
  MABON = 'mabon', // 21 Sep (Norte) / 21 Mar (Sur) - Equinoccio Otoño
}
